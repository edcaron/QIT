    private void selecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecionarActionPerformed
        try {
            String id = (String.valueOf(tabela.getValueAt(tabela.getSelectedRow(), 0)));
            String nome = (String.valueOf(tabela.getValueAt(tabela.getSelectedRow(), 1)));

            switch (relacionado) {
                case 1:
                    telaPai.setRelacionado1(id, nome);
                    break;
                case 2:
                    telaPai.setRelacionado2(id, nome);
                    break;
                case 3:
                    telaPai.setRelacionado3(id, nome);
                    break;
                case 4:
                    telaPai.setRelacionado4(id, nome);
                    break;
            }

            this.dispose();
        } catch (Exception e) {
            System.out.println("Erro ao selecionar: \n " + e);
        }
    }//GEN-LAST:event_selecionarActionPerformed

