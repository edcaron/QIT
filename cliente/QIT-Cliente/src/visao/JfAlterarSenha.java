    @Override
    public boolean editar() {
        if (jSenhaConfirma.getText().equals(jSenha.getText()) && jSenha.getText().length() > 0) {

            user.setSenha(jSenha.getText());
            if (cu.salvar(user)) {
                JOptionPane.showMessageDialog(rootPane, "Senha alterada com sucesso");
                this.dispose();
            }
        } else {

            JOptionPane.showMessageDialog(rootPane, "Confirmação senha de não confere");
            return false;
        }
        return true;
    }

