    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        try {
            // Compila o relatorio
            String diretorio = "src\\reports\\report_software.jrxml";
            JasperReport relatorio = JasperCompileManager.compileReport(diretorio);

            // Mapeia campos de parametros para o relatorio, mesmo que nao existam
            Map parametros = new HashMap();
            int idmaquina = Integer.parseInt(qftfIdMaquina.getText());
            parametros.put("idmaquina", idmaquina);

            // Executa relatoio
            JasperPrint impressao = JasperFillManager.fillReport(relatorio, parametros, ConexaoBD.getInstance().getConnection());

            // Exibe resultado em video
            JasperViewer.viewReport(impressao, false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gerar relat√≥rio: " + e);
        }
    }//GEN-LAST:event_btSalvarActionPerformed

